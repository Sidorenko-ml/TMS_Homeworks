import math

a = int(input('Введите первое число '))
b = int(input('Введите второе число '))
Arifm = ((a+b)/2)
Geometr = math.sqrt(a*b) #Среднее геометрическое(произведение чисел в корне) с помощью math.sqrt(корень)
print('Среднее арифметическое = ', Arifm)
print('Среднее геометрическое = ',Geometr)